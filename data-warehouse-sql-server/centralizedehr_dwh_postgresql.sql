-- 4.1. FACT ENCOUNTER
-- Grain: một lượt khám của một bệnh nhân tại một bệnh viện, do một bác sĩ phụ trách.
CREATE TABLE IF NOT EXISTS dwh.fact_encounter (
    encounter_key          BIGINT GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
    encounter_id_source    UUID NULL,
    visit_date_key         INT NOT NULL,
    patient_key            BIGINT NOT NULL,
    hospital_key           BIGINT NOT NULL,
    doctor_key             BIGINT NOT NULL,
    disease_key            BIGINT NOT NULL,
    encounter_count        INT NOT NULL DEFAULT 1,
    has_lab_result         BOOLEAN NOT NULL DEFAULT FALSE,
    has_imaging_report     BOOLEAN NOT NULL DEFAULT FALSE,
    has_prescription       BOOLEAN NOT NULL DEFAULT FALSE,
    created_at_source      TIMESTAMP(0) NULL,
    loaded_at              TIMESTAMP(0) NOT NULL DEFAULT CURRENT_TIMESTAMP,
    batch_id               VARCHAR(100) NULL,

    CONSTRAINT uq_fact_encounter_source UNIQUE (encounter_id_source),
    CONSTRAINT fk_fact_encounter_dim_date FOREIGN KEY (visit_date_key) REFERENCES dwh.dim_date(date_key),
    CONSTRAINT fk_fact_encounter_dim_patient FOREIGN KEY (patient_key) REFERENCES dwh.dim_patient(patient_key),
    CONSTRAINT fk_fact_encounter_dim_hospital FOREIGN KEY (hospital_key) REFERENCES dwh.dim_hospital(hospital_key),
    CONSTRAINT fk_fact_encounter_dim_doctor FOREIGN KEY (doctor_key) REFERENCES dwh.dim_doctor(doctor_key),
    CONSTRAINT fk_fact_encounter_dim_disease FOREIGN KEY (disease_key) REFERENCES dwh.dim_disease(disease_key)
);

-- 4.2. FACT LAB RESULT
-- Grain: một kết quả xét nghiệm thuộc một lượt khám.
CREATE TABLE IF NOT EXISTS dwh.fact_lab_result (
    lab_result_key       BIGINT GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
    lab_result_id_source UUID NULL,
    encounter_id_source  UUID NULL,
    test_date_key        INT NOT NULL,
    patient_key          BIGINT NOT NULL,
    hospital_key         BIGINT NOT NULL,
    doctor_key           BIGINT NOT NULL,
    test_code            VARCHAR(100) NULL,
    test_name            VARCHAR(255) NULL,
    result_value         VARCHAR(255) NULL,
    unit                 VARCHAR(100) NULL,
    is_abnormal          BOOLEAN NULL,
    lab_result_count     INT NOT NULL DEFAULT 1,
    loaded_at            TIMESTAMP(0) NOT NULL DEFAULT CURRENT_TIMESTAMP,
    batch_id             VARCHAR(100) NULL,

    CONSTRAINT uq_fact_lab_result_source UNIQUE (lab_result_id_source),
    CONSTRAINT fk_fact_lab_result_dim_date FOREIGN KEY (test_date_key) REFERENCES dwh.dim_date(date_key),
    CONSTRAINT fk_fact_lab_result_dim_patient FOREIGN KEY (patient_key) REFERENCES dwh.dim_patient(patient_key),
    CONSTRAINT fk_fact_lab_result_dim_hospital FOREIGN KEY (hospital_key) REFERENCES dwh.dim_hospital(hospital_key),
    CONSTRAINT fk_fact_lab_result_dim_doctor FOREIGN KEY (doctor_key) REFERENCES dwh.dim_doctor(doctor_key)
);

-- 4.3. FACT IMAGING REPORT
-- Grain: một báo cáo chẩn đoán hình ảnh thuộc một lượt khám.
CREATE TABLE IF NOT EXISTS dwh.fact_imaging_report (
    imaging_report_key       BIGINT GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
    imaging_report_id_source UUID NULL,
    encounter_id_source      UUID NULL,
    study_date_key           INT NOT NULL,
    patient_key              BIGINT NOT NULL,
    hospital_key             BIGINT NOT NULL,
    doctor_key               BIGINT NOT NULL,
    modality                 VARCHAR(50) NULL,
    imaging_report_count     INT NOT NULL DEFAULT 1,
    has_pacs_link            BOOLEAN NOT NULL DEFAULT FALSE,
    loaded_at                TIMESTAMP(0) NOT NULL DEFAULT CURRENT_TIMESTAMP,
    batch_id                 VARCHAR(100) NULL,

    CONSTRAINT uq_fact_imaging_report_source UNIQUE (imaging_report_id_source),
    CONSTRAINT fk_fact_imaging_report_dim_date FOREIGN KEY (study_date_key) REFERENCES dwh.dim_date(date_key),
    CONSTRAINT fk_fact_imaging_report_dim_patient FOREIGN KEY (patient_key) REFERENCES dwh.dim_patient(patient_key),
    CONSTRAINT fk_fact_imaging_report_dim_hospital FOREIGN KEY (hospital_key) REFERENCES dwh.dim_hospital(hospital_key),
    CONSTRAINT fk_fact_imaging_report_dim_doctor FOREIGN KEY (doctor_key) REFERENCES dwh.dim_doctor(doctor_key)
);

-- 4.4. FACT PRESCRIPTION
-- Grain: một dòng thuốc được kê trong một lượt khám.
CREATE TABLE IF NOT EXISTS dwh.fact_prescription (
    prescription_key        BIGINT GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
    prescription_id_source  UUID NULL,
    encounter_id_source     UUID NULL,
    prescription_date_key   INT NOT NULL,
    patient_key             BIGINT NOT NULL,
    hospital_key            BIGINT NOT NULL,
    doctor_key              BIGINT NOT NULL,
    drug_key                BIGINT NOT NULL,
    quantity                NUMERIC(18,2) NULL,
    duration_days           INT NULL,
    prescription_line_count INT NOT NULL DEFAULT 1,
    loaded_at               TIMESTAMP(0) NOT NULL DEFAULT CURRENT_TIMESTAMP,
    batch_id                VARCHAR(100) NULL,

    CONSTRAINT uq_fact_prescription_source UNIQUE (prescription_id_source),
    CONSTRAINT ck_fact_prescription_quantity CHECK (quantity IS NULL OR quantity >= 0),
    CONSTRAINT ck_fact_prescription_duration CHECK (duration_days IS NULL OR duration_days > 0),
    CONSTRAINT fk_fact_prescription_dim_date FOREIGN KEY (prescription_date_key) REFERENCES dwh.dim_date(date_key),
    CONSTRAINT fk_fact_prescription_dim_patient FOREIGN KEY (patient_key) REFERENCES dwh.dim_patient(patient_key),
    CONSTRAINT fk_fact_prescription_dim_hospital FOREIGN KEY (hospital_key) REFERENCES dwh.dim_hospital(hospital_key),
    CONSTRAINT fk_fact_prescription_dim_doctor FOREIGN KEY (doctor_key) REFERENCES dwh.dim_doctor(doctor_key),
    CONSTRAINT fk_fact_prescription_dim_drug FOREIGN KEY (drug_key) REFERENCES dwh.dim_drug(drug_key)
);

