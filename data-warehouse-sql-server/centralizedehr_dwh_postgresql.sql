-- 8.1. Tổng lượt khám theo tháng và bệnh viện
CREATE OR REPLACE VIEW mart.vw_encounter_by_month_hospital AS
SELECT
    dd.year_number,
    dd.month_number,
    dh.hospital_name,
    dh.hospital_level,
    SUM(fe.encounter_count) AS total_encounters,
    SUM(CASE WHEN fe.has_lab_result = TRUE THEN 1 ELSE 0 END) AS encounters_with_lab,
    SUM(CASE WHEN fe.has_imaging_report = TRUE THEN 1 ELSE 0 END) AS encounters_with_imaging,
    SUM(CASE WHEN fe.has_prescription = TRUE THEN 1 ELSE 0 END) AS encounters_with_prescription
FROM dwh.fact_encounter fe
JOIN dwh.dim_date dd ON dd.date_key = fe.visit_date_key
JOIN dwh.dim_hospital dh ON dh.hospital_key = fe.hospital_key
GROUP BY
    dd.year_number,
    dd.month_number,
    dh.hospital_name,
    dh.hospital_level;

-- 8.2. Top bệnh / ICD-10
CREATE OR REPLACE VIEW mart.vw_top_disease AS
SELECT
    dis.icd10_code,
    dis.disease_name,
    dis.disease_group,
    SUM(fe.encounter_count) AS total_encounters
FROM dwh.fact_encounter fe
JOIN dwh.dim_disease dis ON dis.disease_key = fe.disease_key
GROUP BY
    dis.icd10_code,
    dis.disease_name,
    dis.disease_group;

-- 8.3. Lượt khám theo bác sĩ / chuyên khoa
CREATE OR REPLACE VIEW mart.vw_encounter_by_doctor_specialty AS
SELECT
    doc.doctor_name,
    doc.specialty,
    dh.hospital_name,
    SUM(fe.encounter_count) AS total_encounters
FROM dwh.fact_encounter fe
JOIN dwh.dim_doctor doc ON doc.doctor_key = fe.doctor_key
JOIN dwh.dim_hospital dh ON dh.hospital_key = fe.hospital_key
GROUP BY
    doc.doctor_name,
    doc.specialty,
    dh.hospital_name;

-- 8.4. Kê đơn thuốc theo tháng và thuốc
CREATE OR REPLACE VIEW mart.vw_prescription_by_month_drug AS
SELECT
    dd.year_number,
    dd.month_number,
    drug.drug_code,
    drug.drug_name,
    drug.drug_group,
    COUNT(*) AS prescription_lines,
    SUM(COALESCE(fp.quantity, 0)) AS total_quantity,
    AVG(fp.duration_days::NUMERIC(18,2)) AS avg_duration_days
FROM dwh.fact_prescription fp
JOIN dwh.dim_date dd ON dd.date_key = fp.prescription_date_key
JOIN dwh.dim_drug drug ON drug.drug_key = fp.drug_key
GROUP BY
    dd.year_number,
    dd.month_number,
    drug.drug_code,
    drug.drug_name,
    drug.drug_group;

-- 8.5. Lịch hẹn theo trạng thái
CREATE OR REPLACE VIEW mart.vw_appointment_status AS
SELECT
    dd.year_number,
    dd.month_number,
    dh.hospital_name,
    fa.appointment_status,
    SUM(fa.appointment_count) AS total_appointments
FROM dwh.fact_appointment fa
JOIN dwh.dim_date dd ON dd.date_key = fa.appointment_date_key
JOIN dwh.dim_hospital dh ON dh.hospital_key = fa.hospital_key
GROUP BY
    dd.year_number,
    dd.month_number,
    dh.hospital_name,
    fa.appointment_status;

-- 8.6. Consent theo trạng thái
CREATE OR REPLACE VIEW mart.vw_consent_status AS
SELECT
    dh.hospital_name,
    fc.consent_status,
    COUNT(*) AS total_consents,
    AVG(fc.valid_duration_days::NUMERIC(18,2)) AS avg_valid_duration_days
FROM dwh.fact_consent fc
JOIN dwh.dim_hospital dh ON dh.hospital_key = fc.hospital_key
GROUP BY
    dh.hospital_name,
    fc.consent_status;

-- 8.7. Mapping bệnh nhân theo bệnh viện
CREATE OR REPLACE VIEW mart.vw_patient_mapping_by_hospital AS
SELECT
    dh.hospital_name,
    dh.hospital_level,
    COUNT(*) AS total_mappings,
    COUNT(DISTINCT fpm.patient_key) AS total_mapped_patients,
    COUNT(DISTINCT fpm.local_patient_id) AS total_local_patient_ids
FROM dwh.fact_patient_mapping fpm
JOIN dwh.dim_hospital dh ON dh.hospital_key = fpm.hospital_key
GROUP BY
    dh.hospital_name,
    dh.hospital_level;

-- 8.8. KPI tổng quan hệ thống
CREATE OR REPLACE VIEW mart.vw_system_kpi_overview AS
SELECT
    (SELECT COUNT(*) FROM dwh.dim_patient WHERE patient_key <> 0 AND is_current = TRUE) AS total_patients,
    (SELECT COUNT(*) FROM dwh.dim_hospital WHERE hospital_key <> 0 AND is_active = TRUE) AS total_hospitals,
    (SELECT COUNT(*) FROM dwh.dim_doctor WHERE doctor_key <> 0 AND is_current = TRUE) AS total_doctors,
    (SELECT COALESCE(SUM(encounter_count), 0) FROM dwh.fact_encounter) AS total_encounters,
    (SELECT COUNT(*) FROM dwh.fact_prescription) AS total_prescription_lines,
    (SELECT COUNT(*) FROM dwh.fact_lab_result) AS total_lab_results,
    (SELECT COUNT(*) FROM dwh.fact_imaging_report) AS total_imaging_reports,
    (SELECT COUNT(*) FROM dwh.fact_appointment) AS total_appointments,
    (SELECT COUNT(*) FROM dwh.fact_consent) AS total_consents,
    CURRENT_TIMESTAMP AS refreshed_at;

