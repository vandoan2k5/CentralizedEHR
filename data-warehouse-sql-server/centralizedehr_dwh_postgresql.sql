-- ============================================================
-- 2. DIMENSION TABLES
-- ============================================================

-- 2.1. DIM DATE
CREATE TABLE IF NOT EXISTS dwh.dim_date (
    date_key        INT PRIMARY KEY,      -- YYYYMMDD, 0 = UNKNOWN
    full_date       DATE NULL,
    day_of_month    SMALLINT NULL,
    month_number    SMALLINT NULL,
    month_name      VARCHAR(20) NULL,
    quarter_number  SMALLINT NULL,
    year_number     SMALLINT NULL,
    week_of_year    SMALLINT NULL,
    is_weekend      BOOLEAN NOT NULL DEFAULT FALSE
);

-- 2.2. DIM PATIENT
CREATE TABLE IF NOT EXISTS dwh.dim_patient (
    patient_key          BIGINT GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
    patient_id_source    UUID NULL,
    identity_hash        VARCHAR(64) NULL,
    insurance_code_hash  VARCHAR(64) NULL,
    gender               VARCHAR(20) NULL,
    date_of_birth        DATE NULL,
    age_group            VARCHAR(50) NULL,
    created_date         DATE NULL,
    effective_from       TIMESTAMP(0) NOT NULL DEFAULT CURRENT_TIMESTAMP,
    effective_to         TIMESTAMP(0) NULL,
    is_current           BOOLEAN NOT NULL DEFAULT TRUE,
    source_system        VARCHAR(100) NOT NULL DEFAULT 'CentralizedEHR',
    loaded_at            TIMESTAMP(0) NOT NULL DEFAULT CURRENT_TIMESTAMP
);

CREATE UNIQUE INDEX IF NOT EXISTS ux_dim_patient_current
ON dwh.dim_patient(patient_id_source)
WHERE is_current = TRUE AND patient_id_source IS NOT NULL;

-- 2.3. DIM HOSPITAL
CREATE TABLE IF NOT EXISTS dwh.dim_hospital (
    hospital_key        BIGINT GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
    hospital_id_source  UUID NULL,
    hospital_code       VARCHAR(50) NULL,
    hospital_name       VARCHAR(255) NOT NULL,
    hospital_level      VARCHAR(50) NULL,
    address             VARCHAR(500) NULL,
    is_active           BOOLEAN NOT NULL DEFAULT TRUE,
    created_date        DATE NULL,
    loaded_at           TIMESTAMP(0) NOT NULL DEFAULT CURRENT_TIMESTAMP
);

CREATE UNIQUE INDEX IF NOT EXISTS ux_dim_hospital_source
ON dwh.dim_hospital(hospital_id_source)
WHERE hospital_id_source IS NOT NULL;

CREATE UNIQUE INDEX IF NOT EXISTS ux_dim_hospital_code
ON dwh.dim_hospital(hospital_code)
WHERE hospital_code IS NOT NULL;

-- 2.4. DIM DOCTOR
CREATE TABLE IF NOT EXISTS dwh.dim_doctor (
    doctor_key          BIGINT GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
    doctor_id_source    UUID NULL,
    practicing_license  VARCHAR(100) NULL,
    doctor_name         VARCHAR(255) NULL,
    specialty           VARCHAR(255) NULL,
    hospital_key        BIGINT NOT NULL DEFAULT 0,
    effective_from      TIMESTAMP(0) NOT NULL DEFAULT CURRENT_TIMESTAMP,
    effective_to        TIMESTAMP(0) NULL,
    is_current          BOOLEAN NOT NULL DEFAULT TRUE,
    loaded_at           TIMESTAMP(0) NOT NULL DEFAULT CURRENT_TIMESTAMP,
    CONSTRAINT fk_dim_doctor_dim_hospital
        FOREIGN KEY (hospital_key) REFERENCES dwh.dim_hospital(hospital_key)
);

CREATE UNIQUE INDEX IF NOT EXISTS ux_dim_doctor_current
ON dwh.dim_doctor(doctor_id_source)
WHERE is_current = TRUE AND doctor_id_source IS NOT NULL;

-- 2.5. DIM DISEASE
CREATE TABLE IF NOT EXISTS dwh.dim_disease (
    disease_key    BIGINT GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
    icd10_code     VARCHAR(50) NOT NULL UNIQUE,
    disease_name   VARCHAR(255) NULL,
    disease_group  VARCHAR(255) NULL,
    description    TEXT NULL,
    loaded_at      TIMESTAMP(0) NOT NULL DEFAULT CURRENT_TIMESTAMP
);

-- 2.6. DIM DRUG
CREATE TABLE IF NOT EXISTS dwh.dim_drug (
    drug_key     BIGINT GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
    drug_code    VARCHAR(100) NOT NULL UNIQUE,
    drug_name    VARCHAR(255) NULL,
    drug_group   VARCHAR(255) NULL,
    description  TEXT NULL,
    metadata     JSONB NULL,
    loaded_at    TIMESTAMP(0) NOT NULL DEFAULT CURRENT_TIMESTAMP
);

-- 2.7. DIM SERVICE - mở rộng cho Finance Mart
CREATE TABLE IF NOT EXISTS dwh.dim_service (
    service_key   BIGINT GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
    service_code  VARCHAR(100) NOT NULL UNIQUE,
    service_name  VARCHAR(255) NULL,
    service_type  VARCHAR(100) NULL,
    description   TEXT NULL,
    loaded_at     TIMESTAMP(0) NOT NULL DEFAULT CURRENT_TIMESTAMP
);

-- 2.8. DIM INSURANCE - mở rộng cho Finance Mart
CREATE TABLE IF NOT EXISTS dwh.dim_insurance (
    insurance_key   BIGINT GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
    insurance_type  VARCHAR(100) NOT NULL UNIQUE,
    coverage_rate   NUMERIC(5,2) NULL,
    description     TEXT NULL,
    loaded_at       TIMESTAMP(0) NOT NULL DEFAULT CURRENT_TIMESTAMP
);

