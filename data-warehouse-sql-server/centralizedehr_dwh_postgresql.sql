INSERT INTO dwh.dim_date (
    date_key, full_date, day_of_month, month_number, month_name,
    quarter_number, year_number, week_of_year, is_weekend
)
SELECT
    TO_CHAR(d::DATE, 'YYYYMMDD')::INT AS date_key,
    d::DATE AS full_date,
    EXTRACT(DAY FROM d)::SMALLINT AS day_of_month,
    EXTRACT(MONTH FROM d)::SMALLINT AS month_number,
    TRIM(TO_CHAR(d, 'Month')) AS month_name,
    EXTRACT(QUARTER FROM d)::SMALLINT AS quarter_number,
    EXTRACT(YEAR FROM d)::SMALLINT AS year_number,
    EXTRACT(WEEK FROM d)::SMALLINT AS week_of_year,
    CASE WHEN EXTRACT(ISODOW FROM d) IN (6, 7) THEN TRUE ELSE FALSE END AS is_weekend
FROM generate_series('2020-01-01'::DATE, '2035-12-31'::DATE, INTERVAL '1 day') AS gs(d)
ON CONFLICT (date_key) DO NOTHING;

