-- 4.9. FACT BILLING - mở rộng khi có module thanh toán.
CREATE TABLE IF NOT EXISTS dwh.fact_billing (
    billing_key               BIGINT GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
    billing_id_source         UUID NULL,
    encounter_id_source       UUID NULL,
    billing_date_key          INT NOT NULL,
    patient_key               BIGINT NOT NULL,
    hospital_key              BIGINT NOT NULL,
    doctor_key                BIGINT NULL,
    service_key               BIGINT NOT NULL,
    insurance_key             BIGINT NOT NULL,
    total_amount              NUMERIC(18,2) NOT NULL DEFAULT 0,
    insurance_covered_amount  NUMERIC(18,2) NOT NULL DEFAULT 0,
    patient_paid_amount       NUMERIC(18,2) NOT NULL DEFAULT 0,
    billing_count             INT NOT NULL DEFAULT 1,
    payment_status            VARCHAR(50) NULL,
    loaded_at                 TIMESTAMP(0) NOT NULL DEFAULT CURRENT_TIMESTAMP,
    batch_id                  VARCHAR(100) NULL,

    CONSTRAINT ck_fact_billing_total CHECK (total_amount >= 0),
    CONSTRAINT ck_fact_billing_paid CHECK (insurance_covered_amount + patient_paid_amount <= total_amount),
    CONSTRAINT fk_fact_billing_dim_date FOREIGN KEY (billing_date_key) REFERENCES dwh.dim_date(date_key),
    CONSTRAINT fk_fact_billing_dim_patient FOREIGN KEY (patient_key) REFERENCES dwh.dim_patient(patient_key),
    CONSTRAINT fk_fact_billing_dim_hospital FOREIGN KEY (hospital_key) REFERENCES dwh.dim_hospital(hospital_key),
    CONSTRAINT fk_fact_billing_dim_doctor FOREIGN KEY (doctor_key) REFERENCES dwh.dim_doctor(doctor_key),
    CONSTRAINT fk_fact_billing_dim_service FOREIGN KEY (service_key) REFERENCES dwh.dim_service(service_key),
    CONSTRAINT fk_fact_billing_dim_insurance FOREIGN KEY (insurance_key) REFERENCES dwh.dim_insurance(insurance_key)
);

