/* ============================================================
 CENTRALIZEDEHR - POSTGRESQL DATA WAREHOUSE / DATA MART SCRIPT
 Chuyển đổi từ bản SQL Server local sang PostgreSQL.

 Cách dùng khuyến nghị:
   1. Tạo database trước trong pgAdmin/DBeaver/psql:
        CREATE DATABASE centralizedehr_dwh;
   2. Kết nối vào database centralizedehr_dwh.
   3. Chạy toàn bộ script này.

 Ghi chú chuyển đổi chính:
   - SQL Server USE/GO/OBJECT_ID/IDENTITY_INSERT được thay bằng cú pháp PostgreSQL.
   - NVARCHAR -> VARCHAR/TEXT, UNIQUEIDENTIFIER -> UUID, DATETIME2 -> TIMESTAMP(0), BIT -> BOOLEAN.
   - BIGINT IDENTITY(1,1) -> BIGINT GENERATED BY DEFAULT AS IDENTITY.
   - Tên bảng/cột đổi sang snake_case để đúng phong cách PostgreSQL.
   - Phần ETL từ OLTP nên chạy sau khi đã có bảng nguồn; mặc định giả định OLTP nằm trong schema public.
============================================================ */

